class Calculator:
    def two_months(self, month_one, month_two):
        return (month_one + month_two) / 2

    def four_months(self, month_one, month_two, month_three, month_four):
        return (month_one + month_two + month_three + month_four) / 4

    def six_months(self, month_one, month_two, month_three, month_four, month_five, month_six):
        return (month_one + month_two + month_three + month_four + month_five + month_six) / 6
