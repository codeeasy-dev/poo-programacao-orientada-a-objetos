namespace Static
{
    class Calculator
    {
        public double avarage(double n1, double n2)
        {
            return (n1 + n2) / 2;
        }

        public double avarage(double n1, double n2, double n3, double n4)
        {
            return (n1 + n2 + (n3 * 2) + n4) / 5;
        }

        public double avarage(double n1, double n2, double n3, double n4, double n5, double n6)
        {
            return (n1 + (n2 * 3) + n3 + (n4 * 2) + n5 + n6) / 9;
        }
    }
}
